
public class AreaRectanguloConVariables {

    public static void main(String[] args) {
        
        double largo, alto, area, perimetro;
        largo = 2.5;
        alto = 4;
        area = largo * alto;
        perimetro = largo * 2 + alto * 2;

        System.out.println("La superficie del rectángulo es: " + area + " cm2");
        System.out.println("El perímetro del rectángulo es: " + perimetro + " cm");
    }

}
